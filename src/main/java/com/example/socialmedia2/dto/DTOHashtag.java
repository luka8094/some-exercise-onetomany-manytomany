package com.example.socialmedia2.dto;

public class DTOHashtag {

    private String hashtag;

    public DTOHashtag(String hashtag) {
        this.hashtag = hashtag;
    }

    public String getHashtag() {
        return hashtag;
    }

    public void setHashtag(String hashtagName) {
        this.hashtag = hashtagName;
    }

}
